package com.skbaby.orange.enums;

public enum ResponseCode {
    SUCCESS(0,""),
    Insert_Error(1,"新增失败"),
    Delete_Error(2,"删除失败");

    private int code;
    private String msg;

    ResponseCode(int code, String msg) {
        this.code = code;
        this.msg = msg;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
